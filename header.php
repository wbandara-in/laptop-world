    function menu_shop(content) {

        if (content == 'menu') {
            document.getElementById("menu-selecter-offcanvas").classList =
                "border border-0 border-bottom border-secondary col-5";
            document.getElementById("shop-selecter-offcanvas").classList = "border-0 col-5";
