            document.getElementById("shop-selecter-offcanvas").classList =
                "border border-0 border-bottom border-secondary col-5";
            document.getElementById("shop-section").classList = "offcanvas-body d-block";
            document.getElementById("menu-section").classList = "offcanvas-body d-none";
        }
    }
